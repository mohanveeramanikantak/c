// Bitwise operators
#include<stdio.h>
int main()
{
	int a=30,b=20;
	printf("\n A and B : %d",a&b);		 // a and b
	printf("\n A or B : %d", a|b);		// a or b
	printf("\n A shift left B : %d",a<<b); 	// a shift left
	printf("\n A Shift Right B : %d",a>>b);	// a shift Right
	printf("\n A ex-or B : %d", a^b);	//l ex-or b
	printf("\n A Complement : %d",~a);	// a completement
	return 0;
	
}
