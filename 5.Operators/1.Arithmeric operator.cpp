// Arithmetric operator
#include<stdio.h>
int main()
{
	int a=5,b=10;
	printf("\n Sum of two Numbers : %d", a+b);
	printf("\n Sub of two Numbers : %d", a-b);
	printf("\n Mul of two Numbers : %d", a*b);
	printf("\n Div of two Numbers : %d", a/b);
	printf("\n Module of two Numbers : %d", a%b);
}
