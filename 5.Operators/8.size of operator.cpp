/*size of operator*/
#include<stdio.h>
int main()
{
	int myInt;
	float myFloat;
	char myChar;
	double myDouble;
	printf("\n\n Size of int is : %Iu",sizeof(myInt));
	printf("\n\n Size of float is :%Iu",sizeof(myFloat));
	printf("\n\n Size of char is :%Iu",sizeof(myChar));
	printf("\n\n Size of myDouble is :%Iu",sizeof(myDouble));
	return 0;
}
