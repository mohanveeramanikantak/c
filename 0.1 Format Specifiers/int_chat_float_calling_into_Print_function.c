//To print other types, use %c for char and %f for float: 
#include <stdio.h>
int main() 
{
  // Create variables
  int myNum = 20;              // Integer (whole number)
  float myFloatNum = 20.99;     // Floating point number
  char myLetter = 'M';         // Character
  
  // Print variables
  printf("%d\n", myNum);
  printf("%f\n", myFloatNum);
  printf("%c\n", myLetter);
  return 0;
}
